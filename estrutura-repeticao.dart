void main() {
  // Exibindo números de 0 a 4
  for (int i = 0; i < 5; i++) {
    print(i);
  }
  // ##############################

  // Exibindo números pares de 0 a 10
  for (int i = 0; i < 10; i += 2) {
    print(i);
  }
  // ##############################

  // Exibindo números de forma decrescente de 5 a 0
  for (int i = 5; i >= 0; i--) {
    print(i);
  }
  // ##############################

  int i = 0;
  while (i < 5) {
    print(i);
    i++;
  }
  // ##############################

  int u = 0;
  do {
    print(u);
    u++;
  } while (u < 5);
  // ##############################
}
